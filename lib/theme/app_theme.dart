    final Color successColor = isLight ? successLight : successDark;
    return GoogleFonts.inter(
      fontSize: fontSize,
      fontWeight: fontWeight,
      color: successColor,
    );
  }

  /// Get error text style for warnings and validation
  static TextStyle getErrorTextStyle({
